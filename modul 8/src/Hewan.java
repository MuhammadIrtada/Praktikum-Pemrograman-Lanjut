class Hewan {
    protected int hargaEkor;
    protected int jumlah;

    public Hewan(int hargaEkor, int jumlah) {
        this.hargaEkor = hargaEkor;
        this.jumlah = jumlah;
    }

    public int getHargaEkor() {
        return hargaEkor;
    }

    public int getJumlah() {
        return jumlah;
    }
}