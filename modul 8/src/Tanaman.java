class Tanaman {
    protected int hargaHektar;
    protected int luas;

    public Tanaman(int hargaHektar, int luas) {
        this.hargaHektar = hargaHektar;
        this.luas = luas;
    }

    public int getHargaHektar() {
        return hargaHektar;
    }

    public int getLuas() {
        return luas;
    }
}
